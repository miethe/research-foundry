# single_user never needs DI-1 acceptance — confirmed with NOTHING configured
# (no ack flag, no audit artifact anywhere) alongside an otherwise-unsafe
# auth.provider — a total no-op exactly as FR-2 requires.
# ---------------------------------------------------------------------------


class TestSingleUserNeverRequiresDi1Acceptance:
    def test_single_user_starts_with_no_di1_state_at_all(self, tmp_foundry):
        _set_foundry_overrides(
            tmp_foundry,
            {"deployment_mode": "single_user", "auth": {"provider": "none"}},
        )
        config = FoundryConfig(paths=tmp_foundry)

        app_instance = create_app(config)  # must not raise

        assert isinstance(app_instance, FastAPI)

    def test_single_user_di1_audit_acknowledged_accessor_default_false(self, tmp_foundry):
        """Sanity check on the underlying accessor too — single_user's
        no-op is because the gate never evaluates (d) for this mode, not
        because the flag happens to default True."""
        config = FoundryConfig(paths=tmp_foundry)
        assert config.di1_audit_acknowledged() is False


