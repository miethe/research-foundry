# Live session: once the gate passes, the composite auth chain (ACT-203)
# resolves a service-account Bearer token to the SA identity for real, over
# the actual ASGI stack (not a unit-level mock of the middleware).
# ---------------------------------------------------------------------------


@pytest.fixture()
def gate_passed_app(tmp_foundry, tmp_path):
    """A fully gate-passing ``multi_user`` app + its backing config."""
    config = _multi_user_config(tmp_foundry, tmp_path, di1_ack=True, audit_status="accepted")
    app_instance = create_app(config)
    client = TestClient(app_instance, raise_server_exceptions=True)
    return client, config


class TestLiveSessionCompositeAuthChain:
    def test_service_account_token_resolves_to_sa_identity(self, gate_passed_app):
        client, config = gate_passed_app
        issued = _seed_default_service_account(config)

        resp = client.get(
            "/api/auth/identity",
            headers={"Authorization": f"Bearer {issued.plaintext}"},
        )

        assert resp.status_code == 200
        body = resp.json()
        assert body["user_id"] == _DEFAULT_SA_ID
        assert body["workspace_id"] == _DEFAULT_SA_WORKSPACE
        assert "researcher" in body["roles"]

    def test_no_credential_still_401_under_passed_gate(self, gate_passed_app):
        """The gate passing is orthogonal to per-request auth — a live
        multi_user session still rejects unauthenticated callers."""
        client, _config = gate_passed_app
        resp = client.get("/api/auth/identity")
        assert resp.status_code == 401

    def test_revoked_service_account_token_401_under_passed_gate(self, gate_passed_app):
        client, config = gate_passed_app
        issued = _seed_default_service_account(config)
        token_service.revoke_token(config.paths, issued.token_id)

        resp = client.get(
            "/api/auth/identity",
            headers={"Authorization": f"Bearer {issued.plaintext}"},
        )

        assert resp.status_code == 401

    def test_health_exempt_under_passed_multi_user_gate(self, gate_passed_app):
        client, _config = gate_passed_app
        resp = client.get("/health")
        assert resp.status_code == 200


# ---------------------------------------------------------------------------
# Live session: agent-job service-account identity binding (ACT-204, FR-12)
# actually activates under this same gate-passed multi_user config/paths.
# ---------------------------------------------------------------------------


class TestLiveSessionAgentJobServiceAccountBinding:
    def test_agent_job_binds_to_sa_execution_identity_and_audit_records_both(
        self, gate_passed_app
    ):
        client, config = gate_passed_app
        _seed_default_service_account(config)

        # Launch the job against the EXACT same on-disk paths the live app
        # is serving from — proving the binding is real for this session,
        # not merely a config-level unit assertion in isolation.
        service = AgentJobService(config.paths)
        job = service.create_job(
            provider="claude_agent_sdk",
            model_profile="rf_synthesize_deep",
            request_kind="research",
            policy_snapshot=dict(_MINIMAL_POLICY_SNAPSHOT),
            project_id="test-project",
            workspace_id=_DEFAULT_SA_WORKSPACE,
            created_by="human_caller_alice",
        )

        assert job.created_by == _DEFAULT_SA_ID

        events = list_events(config.paths, mutation_type="agent_job_launched")
        matching = [e for e in events["items"] if e["target_ref"] == job.agent_job_id]
        assert len(matching) == 1
        event = matching[0]
        assert event["actor_user_id"] == _DEFAULT_SA_ID
        assert event["actor_workspace_id"] == _DEFAULT_SA_WORKSPACE
        snapshot = event["policy_snapshot"]
        assert snapshot["triggering_identity"] == "human_caller_alice"
        assert snapshot["executing_identity"] == _DEFAULT_SA_ID
        assert snapshot["deployment_mode"] == "multi_user"

        # And the SA's own token still resolves live through the same app —
        # tying the "who executed this" audit identity back to a credential
        # that a real caller could authenticate as in this session.
        issued = token_service.issue_service_account_token(
            config.paths, service_account_id=_DEFAULT_SA_ID
        )
        resp = client.get(
            "/api/auth/identity",
            headers={"Authorization": f"Bearer {issued.plaintext}"},
        )
        assert resp.status_code == 200
        assert resp.json()["user_id"] == event["actor_user_id"]

    def test_without_default_service_account_configured_binding_stays_off(
        self, tmp_foundry, tmp_path
    ):
        """Regression companion (AC-5): even with the DI-1 gate passed and
        multi_user active, the SA binding is a SEPARATE opt-in
        (``agents.default_service_account_id``) — omitting it leaves
        agent-job identity binding untouched, gate or no gate."""
        config = _multi_user_config(
            tmp_foundry,
            tmp_path,
            di1_ack=True,
            audit_status="accepted",
            bind_default_service_account=False,
        )
        create_app(config)  # gate still passes independently of this knob

        service = AgentJobService(config.paths)
        job = service.create_job(
            provider="claude_agent_sdk",
            model_profile="rf_synthesize_deep",
            request_kind="research",
            policy_snapshot=dict(_MINIMAL_POLICY_SNAPSHOT),
            project_id="test-project",
            workspace_id=_DEFAULT_SA_WORKSPACE,
            created_by="human_caller_alice",
        )

        assert job.created_by == "human_caller_alice"
        events = list_events(config.paths, mutation_type="agent_job_launched")
        assert events["items"] == []
